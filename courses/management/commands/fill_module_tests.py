def build_generic_questions(module_title: str, rng: random.Random) -> list[dict]:
    qs = []
    for tpl in GENERIC_TEMPLATES:
        qs.append({
            "text": tpl["tpl"].format(m=module_title),
            "choices": list(tpl["choices"]),
            "correct": tpl["correct"],
            "explanation": tpl["explanation"],
        })
    rng.shuffle(qs)
    return qs


class Command(BaseCommand):
    help = "Generate real module test questions programmatically"

    def add_arguments(self, parser):
        parser.add_argument("--course", default="zhalpy-pedagogika")
        parser.add_argument("--modules", nargs="+", type=int, default=None)
        parser.add_argument("--all", action="store_true")

    @transaction.atomic
    def handle(self, *args, **opts):
        try:
            course = Course.objects.get(slug=opts["course"])
        except Course.DoesNotExist:
            raise CommandError(f"Course '{opts['course']}' not found")

        modules = list(course.modules.all().order_by("order"))
        if opts["modules"]:
            modules = [m for m in modules if m.order in opts["modules"]]
        elif not opts["all"]:
            raise CommandError("Pass --all or --modules N N N")

        ok = 0
        for m in modules:
            quiz = Quiz.objects.filter(module=m, kind=Quiz.Kind.MODULE).first()
            if not quiz:
                continue
            rng = random.Random(quiz.id)
            qs = MODULE_QUESTIONS.get(m.order) or build_generic_questions(m.title, rng)
            # wipe existing questions
            Question.objects.filter(quiz=quiz).delete()
            for i, q in enumerate(qs, 1):
                # Normalize: accept both dict and tuple (text, choices, correct, explanation)
                if isinstance(q, tuple):
                    text, choices, correct, explanation = q
                else:
                    text, choices, correct, explanation = q["text"], q["choices"], q["correct"], q["explanation"]
