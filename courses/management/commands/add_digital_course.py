class Command(BaseCommand):
    help = "Add a second compact course: Digital Pedagogy (3 modules)"

    def add_arguments(self, parser):
        parser.add_argument("--force", action="store_true", help="Delete & recreate if exists")

    @transaction.atomic
    def handle(self, *args, **opts):
        existing = Course.objects.filter(slug=COURSE["slug"]).first()
        if existing:
            if not opts["force"]:
                self.stdout.write(self.style.WARNING(
                    f"Course «{existing.title}» already exists. Use --force to recreate."
                ))
                return
            existing.delete()
            self.stdout.write(f"  removed existing course")

        course = Course.objects.create(
            slug=COURSE["slug"],
            title=COURSE["title"],
            short_description=COURSE["short_description"],
            description=COURSE["description"],
            level=COURSE["level"],
            language="kk",
            duration_hours=COURSE["duration_hours"],
            tags=COURSE["tags"],
            is_published=True,
            has_final_exam=True,
            final_exam_pass_pct=70,
        )
        self.stdout.write(self.style.SUCCESS(f"✓ created course «{course.title}»"))

        # Modules + lessons
        for m_def in MODULES:
            module = Module.objects.create(
                course=course,
                title=m_def["title"],
                summary=m_def["summary"],
                order=m_def["order"],
                has_test=True,
            )
            for idx, l_title in enumerate(m_def["lessons"], 1):
                lesson = Lesson.objects.create(
                    module=module,
                    title=l_title,
                    slug=f"l{m_def['order']}-{idx}",
                    order=idx,
                    summary=f"{m_def['title']} модулінің {idx}-сабағы",
                    content=[],  # filled below
                    duration_minutes=12,
                    is_free_preview=(m_def["order"] == 1 and idx == 1),
                )
                lesson.content = build_lesson_content(lesson)
                lesson.save(update_fields=["content"])
            self.stdout.write(f"  ✓ M{m_def['order']} «{m_def['title']}»  ({len(m_def['lessons'])} lessons)")

        # Module quizzes
        for m_def in MODULES:
            module = course.modules.get(order=m_def["order"])
            quiz = Quiz.objects.create(
                course=course, module=module, kind=Quiz.Kind.MODULE,
                title=f"«{m_def['title']}» модулінің тесті",
                description="Модульді бекіту үшін тест.",
                pass_pct=70, cooldown_minutes=30,
            )
            for i, (text, choices, correct, expl) in enumerate(MODULE_QUESTIONS[m_def["order"]], 1):
                q = Question.objects.create(
                    quiz=quiz, kind=Question.Kind.SINGLE,
                    text=text, explanation=expl, points=1, order=i,
                )
                for j, ch in enumerate(choices):
                    Choice.objects.create(question=q, text=ch, is_correct=(j == correct), order=j + 1)
            self.stdout.write(f"  ✓ quiz M{m_def['order']}: 10 questions")

        # Final exam
        final = Quiz.objects.create(
            course=course, module=None, kind=Quiz.Kind.FINAL,
            title=f"«{course.title}» — финалдық емтихан",
            description="Барлық 3 модульді қамтитын қорытынды емтихан.",
            time_limit_minutes=45, pass_pct=70, cooldown_minutes=60 * 24,
        )
        for i, (text, choices, correct) in enumerate(FINAL_EXAM_QUESTIONS, 1):
            q = Question.objects.create(
                quiz=final, kind=Question.Kind.SINGLE,
                text=text, explanation="", points=2, order=i,
            )
            for j, ch in enumerate(choices):
                Choice.objects.create(question=q, text=ch, is_correct=(j == correct), order=j + 1)
        self.stdout.write(f"  ✓ final exam: {len(FINAL_EXAM_QUESTIONS)} questions")

        # Cases
        modules_by_order = {m.order: m for m in course.modules.all()}
        for module_order, cs in CASES:
            CaseStudy.objects.create(
                course=course, module=modules_by_order[module_order],
                title=cs["title"], situation=cs["situation"],
                questions=cs["questions"], expert_analysis=cs["expert_analysis"],
                rubric={}, difficulty=cs["difficulty"],
                min_words=150, is_published=True,
            )
        self.stdout.write(f"  ✓ {len(CASES)} cases added")

        self.stdout.write("")
        self.stdout.write(self.style.SUCCESS(
            f"DONE. Course «{course.title}» — open at /courses/{course.slug}/"
        ))
