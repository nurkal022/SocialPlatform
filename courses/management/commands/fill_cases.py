class Command(BaseCommand):
    help = "Add curated case studies, distributed across modules"

    def add_arguments(self, parser):
        parser.add_argument("--course", default="zhalpy-pedagogika")
        parser.add_argument("--all", action="store_true")
        parser.add_argument("--keep-existing", action="store_true",
                            help="Don't delete existing cases, append only new ones")

    @transaction.atomic
    def handle(self, *args, **opts):
        try:
            course = Course.objects.get(slug=opts["course"])
        except Course.DoesNotExist:
            raise CommandError(f"Course '{opts['course']}' not found")

        modules_by_order = {m.order: m for m in course.modules.all()}

        if not opts["keep_existing"]:
            n = CaseStudy.objects.filter(course=course).count()
            CaseStudy.objects.filter(course=course).delete()
            self.stdout.write(f"  removed {n} existing case(s)")

        ok = 0
        for module_order, cs in CASES:
            module = modules_by_order.get(module_order)
            if not module:
                continue
            CaseStudy.objects.create(
                course=course,
                module=module,
                title=cs["title"],
                situation=cs["situation"],
                questions=cs["questions"],
                expert_analysis=cs["expert_analysis"],
                rubric={},
                difficulty=cs.get("difficulty", "medium"),
                min_words=150,
                is_published=True,
            )
            self.stdout.write(self.style.SUCCESS(
                f"  ✓ M{module_order}: «{cs['title'][:50]}»"
            ))
            ok += 1

        self.stdout.write("")
        self.stdout.write(self.style.SUCCESS(f"DONE. cases added={ok}"))
