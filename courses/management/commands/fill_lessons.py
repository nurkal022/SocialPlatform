def short_term(title: str) -> tuple[str, str]:
    """Pick a key noun from the title to use as the lesson's term, with a definition."""
    # Look for a word that matches one of our predefined terms (case-insensitive)
    title_lower = title.lower()
    for name, defin in TERM_DEFINITIONS:
        if name.lower().split()[0] in title_lower or name.lower() in title_lower:
            return name, defin
    # Fallback: pick a random term that's broadly relevant
    return random.choice(TERM_DEFINITIONS)


def build_lesson_content(lesson) -> list[dict]:
    rng = random.Random(lesson.id)  # deterministic per lesson
    title = lesson.title.strip().rstrip(".")
    # Compact title for templates (max 80 chars)
    t_short = title if len(title) <= 80 else title[:77] + "..."

    intro = rng.choice(INTRO_TEMPLATES).format(t=t_short)
    term_name, term_def = short_term(title)
    body1, body2 = rng.sample(BODY_FRAGMENTS, 2)
    body1 = body1.format(t=t_short)
    body2 = body2.format(t=t_short)
    callout = rng.choice(CALLOUTS).format(t=t_short)

    q1, q2 = rng.sample(QUESTION_BANK, 2)
    q1 = {
        "type": "question",
        "value": {
            "text": q1["tpl"].format(t=t_short),
            "choices": list(q1["choices"]),
            "correct": q1["correct"],
            "explanation": q1["explanation"],
        },
    }
    q2 = {
        "type": "question",
        "value": {
            "text": q2["tpl"].format(t=t_short),
            "choices": list(q2["choices"]),
            "correct": q2["correct"],
            "explanation": q2["explanation"],
        },
    }

    practical_intro = rng.choice(PRACTICAL_INTROS).format(t=t_short)
    practical_steps = rng.choice(PRACTICAL_STEPS)
    conclusion = rng.choice(CONCLUSIONS).format(t=t_short)

    return [
        {"type": "paragraph", "value": intro},
        {"type": "term", "value": {"name": term_name, "definition": term_def}},

        {"type": "heading", "value": "Негізгі мазмұны"},
        {"type": "paragraph", "value": body1},
        {"type": "paragraph", "value": body2},
        {"type": "callout", "value": callout},

        q1,

        {"type": "heading", "value": "Практикалық қолданысы"},
        {"type": "paragraph", "value": practical_intro},
        {"type": "list", "value": practical_steps},

        q2,

        {"type": "paragraph", "value": conclusion},
    ]


class Command(BaseCommand):
    help = "Fill lesson content programmatically from Kazakh templates (no API needed)"

    def add_arguments(self, parser):
        parser.add_argument("--course", default="zhalpy-pedagogika")
        parser.add_argument("--modules", nargs="+", type=int, default=None)
        parser.add_argument("--all", action="store_true")
        parser.add_argument("--force", action="store_true",
                            help="Overwrite lessons that already have substantial content")

    def handle(self, *args, **opts):
        try:
            course = Course.objects.get(slug=opts["course"])
        except Course.DoesNotExist:
            raise CommandError(f"Course '{opts['course']}' not found")

        lessons_q = Lesson.objects.filter(module__course=course).select_related("module")
        if opts["modules"]:
            lessons_q = lessons_q.filter(module__order__in=opts["modules"])
        elif not opts["all"]:
            raise CommandError("Pass --modules 5 6 7 8 9 10  OR  --all")
        lessons = list(lessons_q.order_by("module__order", "order"))

        ok = skipped = 0
        for ln in lessons:
            has_real = any(
                isinstance(b, dict) and b.get("type") == "paragraph"
                and len(str(b.get("value", ""))) > 150
                for b in (ln.content or [])
            )
            if has_real and not opts["force"]:
                skipped += 1
                self.stdout.write(f"  SKIP M{ln.module.order}.{ln.order} {ln.title[:50]}")
                continue

            ln.content = build_lesson_content(ln)
            ln.save(update_fields=["content", "updated_at"])
            ok += 1
            self.stdout.write(
                self.style.SUCCESS(f"  ✓ M{ln.module.order}.{ln.order} {ln.title[:60]}  ({len(ln.content)} blocks)")
            )

        self.stdout.write("")
        self.stdout.write(self.style.SUCCESS(f"DONE. filled={ok}  skipped={skipped}"))
